public class RectangleD {
    public double x = 0;
    public double y = 0;
    public double width = 0;
    public double height = 0;
    public RectangleD(double newX, double newY, double newWidth, double newHeight) {
        x = newX;
        y = newY;
        width = newWidth;
        height = newHeight;
    }
}
